package kg.santechmarket.enums;

/**
 * Статус пользователя в системе
 */
public enum UserStatus {
    /**
     * На модерации - пользователь зарегистрировался, ждёт одобрения менеджера
     */
    PENDING,

    /**
     * Одобрен - может полноценно пользоваться системой
     */
    APPROVED,

    /**
     * Отклонён - регистрация отклонена менеджером
     */
    REJECTED,

    /**
     * Заблокирован - доступ заблокирован администратором
     */
    BLOCKED
}
